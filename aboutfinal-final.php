            <h2>History of the Business</h2>
            <p>Since the majority of businesses what the owner see in their barangay are <i>sari-sari stores</i>, vegetable markets, fish markets, <i>ukay-ukay</i>, selling pancit/spaghetti/bihon, and many more, the owner first considered what kind of business
