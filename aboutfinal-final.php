            <h2>What is NJS Printing Services?</h2>

            <p>
                NJS Printing Services is owned and managed by Nicole Jane G. Sarmiento and and was established on February 18, 2022. The shop is located at 069 Real St. Calatagan Tibang, Virac, Catanduanes. The business was established to make it easier and less time-consuming
                for the residents of the community to travel to another printing shop outside of the barangay. The business began in just simple printing, until the owner brought some additional materials that can add up to business like other type of
                papers (photopapers etc.), laminating machine, and studio lights for rush ID. All of these services are available at the present.
                <br> <br> Printing, scanning, rush ID, photocopying, name-tagging, and laminating are among the services the business provides. Plain paper, photo paper, and special/matte paper can all be printed on. There are many different sizes available
                for photo printing including A4, 5R, 4R, 3R, 2R, wallet size, and cute size. Each page of scanning costs five pesos (from hardcopy to softcopy). Images or ID pictures can also be scanned; for instance, if a customer wants his 1x1 ID to
                become a 2x2 ID picture, the cost of scanning these ID pictures is same to the cost of a rush ID. Name-tagging's prices depends on it's sizes as well as its customized designs.

                <br> <br> In addition, individuals who reside a great distance from the shop's location can simply access their services by sending a service request along with the services they wish to use. Only pick-ups are the option for delivery since
                we are lack of personal vehicle for transportation.
